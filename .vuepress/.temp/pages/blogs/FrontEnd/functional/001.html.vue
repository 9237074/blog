<p>函数式编程的思维方式：把现实世界的事物和事物之间的联系抽象到程序世界（对运算过程进行抽象）</p>
<ul>
<li>程序的本质：根据输入通过某种运算获得相应的输出，程序开发过程中会涉及很多有输入和输出的函数</li>
<li>x -&gt; f(联系、映射) -&gt; y = f(x)</li>
<li>函数式编程中的函数指的不是程序中的函数，而是数学中的函数，即映射关系</li>
<li>相同的输入始终要得到相同的输出（纯函数）</li>
<li>函数式编程用来描述数据（函数）之间的映射</li>
</ul>
